    public function handle(): int
    {
        $dryRun = $this->option('dry-run');
        $processed = 0;
        $skipped = 0;

        DB::beginTransaction();

        try {
            foreach ($this->bookAuthorMap as $bookId => $combinedAuthors) {
                $names = array_filter(array_map('trim', explode(',', $combinedAuthors)));

                if (count($names) < 2) {
                    $skipped++;
                    continue;
                }

                // Find the current contract(s) for this book
                $contracts = DB::table('author_book_contracts')
                    ->where('book_id', $bookId)
                    ->pluck('id');

                if ($contracts->isEmpty()) {
                    $this->warn("Book {$bookId}: no contracts found, skipping");
                    $skipped++;
                    continue;
                }

                // Find or create each individual author
                $authorIds = [];
                foreach ($names as $i => $name) {
                    $existing = DB::table('authors')
                        ->where('full_name', $name)
                        ->value('id');

                    if ($existing) {
                        $authorIds[] = ['id' => $existing, 'is_new' => false];
                    } else {
                        if (!$dryRun) {
                            $newId = DB::table('authors')->insertGetId([
                                'full_name'  => $name,
                                'created_at' => now(),
                                'updated_at' => now(),
                            ]);
                            $authorIds[] = ['id' => $newId, 'is_new' => true];
                        } else {
                            $authorIds[] = ['id' => 'NEW: ' . $name, 'is_new' => true];
                        }
                    }
                }

                // For each contract, replace the single combined-author row
                // with individual rows (first author = representative)
                foreach ($contracts as $contractId) {
                    // Remove the old combined author row (if it's the blob name)
                    $oldAuthor = DB::table('contract_authors')
                        ->join('authors', 'authors.id', '=', 'contract_authors.author_id')
                        ->where('contract_authors.contract_id', $contractId)
                        ->whereRaw("authors.full_name LIKE '%,%'")
                        ->select('contract_authors.id as ca_id', 'authors.id as author_id')
                        ->first();

                    if ($oldAuthor && !$dryRun) {
                        DB::table('contract_authors')->where('id', $oldAuthor->ca_id)->delete();
                    }

                    // Insert individual rows
                    foreach ($authorIds as $i => $author) {
                        $this->line("  Book {$bookId} / Contract {$contractId}: author[{$i}] = {$author['id']}" . ($author['is_new'] ? ' [NEW]' : ''));

                        if (!$dryRun && is_int($author['id'])) {
                            // Check not already linked
                            $alreadyLinked = DB::table('contract_authors')
                                ->where('contract_id', $contractId)
                                ->where('author_id', $author['id'])
                                ->exists();

                            if (!$alreadyLinked) {
                                DB::table('contract_authors')->insert([
                                    'contract_id'       => $contractId,
                                    'author_id'         => $author['id'],
                                    'is_representative' => ($i === 0) ? 1 : 0,
                                    'created_at'        => now(),
                                    'updated_at'        => now(),
                                ]);
                            }
                        }
                    }
                }

                $processed++;
            }

            if ($dryRun) {
                $this->info("[DRY RUN] Would process {$processed} books, skip {$skipped}");
                DB::rollBack();
            } else {
                DB::commit();
                $this->info("Done. Processed: {$processed}, Skipped: {$skipped}");
            }

        } catch (\Throwable $e) {
            DB::rollBack();
            $this->error('Migration failed: ' . $e->getMessage());
            return self::FAILURE;
        }

        return self::SUCCESS;
    }
}